package Animals

open class Animal(var type: String = "", var volume: Int = 0)

class Cat(type: String, volume: Int) : Animal(type,volume)

class Dog(type: String, volume: Int) : Animal(type,volume)

class Dolphin(type: String, volume: Int) : Animal(type,volume)

class Goat(type: String, volume: Int) : Animal(type,volume)

class Hippo(type: String, volume: Int) : Animal(type,volume)

class Owl(type: String, volume: Int) : Animal(type,volume)

class Penguin(type: String, volume: Int) : Animal(type,volume)

class Sloth(type: String, volume: Int) : Animal(type,volume)

